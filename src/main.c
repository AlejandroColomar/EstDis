/******************************************************************************
 ******* static functions *****************************************************
 ******************************************************************************/
static	void	init_all	(int argc, char *argv[])
{
	/* Init curses */
	alx_start_curses();
	alx_pause_curses();

	/* Init modules */
	about_init();

	/* Parse command line options */
	parser(argc, argv);
}

static	void	cleanup		(void)
{
	/* End curses */
	alx_resume_curses();
	alx_end_curses();
}


